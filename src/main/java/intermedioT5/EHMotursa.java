public class EHMotursa {

    private String noNit;
    private String nombre;
    private String direccion;
    private String telefono;
    private double salarioMensual;
    private double decuentoRenta;

    public EHMotursa() {
    }

    public String getNoNit() {
        return noNit;
    }

    public void setNoNit(String noNit) {
        this.noNit = noNit;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public double getSalarioMensual() {
        return salarioMensual;
    }

    public void setSalarioMensual(double salarioMensual) {
        this.salarioMensual = salarioMensual;
    }

    public double getDecuentoRenta() {
        return decuentoRenta;
    }

    public void setDecuentoRenta(double decuentoRenta) {
        this.decuentoRenta = decuentoRenta;
    }


}
